package com.example.cinestudiar.beans;

public class BComprafuncion {
    public int idfuncion;
    public int idcomprafuncion;
    public int cantidad_por_funcion;
    public String asistencia;
    public String calificacion;

    public int getIdfuncion() {
        return idfuncion;
    }

    public void setIdfuncion(int idfuncion) {
        this.idfuncion = idfuncion;
    }

    public int getIdcomprafuncion() {
        return idcomprafuncion;
    }

    public void setIdcomprafuncion(int idcomprafuncion) {
        this.idcomprafuncion = idcomprafuncion;
    }

    public int getCantidad_por_funcion() {
        return cantidad_por_funcion;
    }

    public void setCantidad_por_funcion(int cantidad_por_funcion) {
        this.cantidad_por_funcion = cantidad_por_funcion;
    }

    public String getAsistencia() {
        return asistencia;
    }

    public void setAsistencia(String asistencia) {
        this.asistencia = asistencia;
    }

    public String getCalificacion() {
        return calificacion;
    }

    public void setCalificacion(String calificacion) {
        this.calificacion = calificacion;
    }
}
